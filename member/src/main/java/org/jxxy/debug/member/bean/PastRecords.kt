package org.jxxy.debug.member.bean

class PastRecords() {
    val resourceId: Long? = null
    val resourceLabel: String? = null
    val resourceTitle: String? = null
    val resourceAuthorName: String? = null
    val resourceAuthorHead: String? = null
    val resourceType: Long? = null
    val createTime: String = ""
    override fun toString(): String {
        return "PastRecords(resourceId=$resourceId, resourceLabel=$resourceLabel, resourceTitle=$resourceTitle, resourceAuthorName=$resourceAuthorName, resourceAuthorHead=$resourceAuthorHead, resourceType=$resourceType)"
    }
}
